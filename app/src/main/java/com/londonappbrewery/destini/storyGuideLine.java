package com.londonappbrewery.destini;

public class storyGuideLine {

    int mStory;
    int mAns1;
    int mAns2;


    public storyGuideLine(int story, int ans1, int ans2){

        mStory=story;
        mAns1=ans1;
        mAns2=ans2;



    }

    public int getStory() {
        return mStory;
    }

    public void setStory(int story) {
        mStory = story;
    }

    public int getAns1() {
        return mAns1;
    }

    public void setAns1(int ans1) {
        mAns1 = ans1;
    }

    public int getAns2() {
        return mAns2;
    }

    public void setAns2(int ans2) {
        mAns2 = ans2;
    }












}
